USE COBMAISDW
SELECT 
  CON.PES_NOME							    AS NOME
 ,CRE_NOME									AS CARTEIRA
 ,CON.PES_CPFCNPJ							AS CPF
 ,CON.CON_NUMERO							    AS CONTRATO
--,CMA.MAR_NOME
,ISNULL(SUB6.MAR_NOME,'SEM UF') UF
,ISNULL(SUB5.MAR_NOME,'SEM CARTEIRA') CARTEIRA
--,REPLACE (CON.VALOR_ABERTO,'.',',')
FROM [DBO].[DW_CONTRATO]	   AS CON WITH (NOLOCK)
--LEFT JOIN DW_CONTRATO_MARCADOR  AS CMA WITH (NOLOCK)ON CMA.CON_ID = CON.CON_ID
LEFT JOIN ( SELECT CON.CON_ID, CMA.MAR_NOME FROM 
			DW_CONTRATO AS CON
			LEFT JOIN DW_CONTRATO_MARCADOR  AS CMA WITH (NOLOCK)ON CMA.CON_ID = CON.CON_ID
			WHERE 1 = 1
			AND CMA.MAR_ID IN ('21338',	'21340','21339') ) AS SUB5 ON SUB5.CON_ID = CON.CON_ID  -- BUSCA CSRTEIRA
LEFT JOIN ( SELECT CON.CON_ID, CMA.MAR_NOME FROM 
			DW_CONTRATO AS CON
			LEFT JOIN DW_CONTRATO_MARCADOR  AS CMA WITH (NOLOCK)ON CMA.CON_ID = CON.CON_ID
			WHERE 1 = 1
			AND CMA.MAR_ID IN('21366','21343','21367','21364','21368','21341','21344','21369','21365') ) AS SUB6 ON SUB6.CON_ID = CON.CON_ID  -- BUSCA UF
WHERE 1=1
--AND CON.PES_CPFCNPJ IN ('07238518000107')
AND CON.CRE_ID in ('28331028')
AND CON.EST_ID NOT  IN (4,3)
ORDER BY 1

